#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	if (dst > src && dst < src + len)
	{
		while (len > 0)
		{
			((char *)dst)[len - 1] = ((char *)src)[len - 1];
			len--;
		}
	}
	else
	{
		return (ft_memcpy(dst, src, len));
	}
	return (dst);
}
